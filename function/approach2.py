def addition():
	#taking input--- inside of Function Body
	a=int(input("Enter First value:"))
	b=int(input("Enter Second value:"))
	#processing inside of Finction Body
	c=a+b         
	#Give output--inside of Function Body
	print("{} + {}={}".format(a,b,c))

#main program
addition()
